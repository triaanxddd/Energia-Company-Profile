     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\About  $about
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, About $about)
    {
        $validatedData = $request->validate([
            'title' => 'required',
            'picture' => 'mimes:png,jpg,pdf|file|max:20480',
            'description' => 'required'
        ]);

        if ($request->hasFile('picture')) {
            if ($request->oldPicture) {
                Storage::delete($request->oldPicture);
            }
            $validatedData['picture'] = $request->file('picture')->store('post-pictures', 'public');
        }
    
        $about->update($validatedData);
    
        return redirect()->back()->with('success', 'Halaman About Us telah diubah');
    }

